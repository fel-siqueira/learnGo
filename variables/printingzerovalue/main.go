package main

import "fmt"

func main() {
	// Variable declaration (without assigning a value)
	var a string
	var b int
	var c float64
	var d bool

	fmt.Printf("Printing the zero-values \n")
	fmt.Printf("String: %v \n", a)
	fmt.Printf("Integer: %v \n", b)
	fmt.Printf("Float: %v \n", c)
	fmt.Printf("Boolean: %v \n", d)
}
